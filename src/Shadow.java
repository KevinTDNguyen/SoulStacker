public class Shadow {

    String name, playerClass;
    int hp, atkDamage, defense;

    public Shadow(String name, int hp, int atkDamage, int defense, String playerClass){
        this.name = name;
        this.hp = hp;
        this.defense = defense;
        this.atkDamage = atkDamage;
        this.playerClass = playerClass;
    }

}
